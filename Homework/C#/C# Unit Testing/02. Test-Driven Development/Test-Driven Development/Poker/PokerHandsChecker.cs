        public bool IsFourOfAKind(IHand hand)
        {
            IsValidHand(hand);

            var isFourOfAKind = hand.Cards.GroupBy(x => x.Face)
                                          .Count(x => x.Count() == 4);

            return isFourOfAKind == 1 ? true : false;
